// HARD: create 4 functions (add, subtract, multiply, and divide)

//add function
function add(num1, num2) {
    let res = num1 + num2
    return res   
}
console.log ('add func: ' + add(10, 5)); //console will show "add func: 15"

//subtraction function
function subtract(num1, num2) {
    let res = num1 - num2
    return res  
}
console.log ('sub func: ' + subtract(10, 5)); //console will show "sub func: 5"

//multiplication function
function multiply(num1, num2) {
    let res = num1 * num2
    return res  
}
console.log ('mul func: ' + multiply(10, 5)); //console will show "mul func: 50"

//division function
function divide(num1, num2) {
    let res = num1 / num2
    return res  
}
console.log ('div func: ' + divide(10, 5)); //console will show "div func: 2"